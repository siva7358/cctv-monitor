def ping_ip(ip):
    """Ping a single IP with timeout"""
    try:
        param = '-n' if platform.system().lower() == 'windows' else '-c'
        cmd = ['ping', param, '1', '-W', '1', ip]
        result = subprocess.run(cmd, stdout=subprocess.DEVNULL, stderr=subprocess.DEVNULL, timeout=2)
        return result.returncode == 0
    except:
        return False

def ping_all_ips():
    """Ping all IPs in parallel"""
    results = {}
    with ThreadPoolExecutor(max_workers=50) as executor:
        future_to_ip = {executor.submit(ping_ip, ip): ip for jb in jb_data for ip in jb['ips']}
        for future in as_completed(future_to_ip):
            ip = future_to_ip[future]
            results[ip] = future.result()
    return results

